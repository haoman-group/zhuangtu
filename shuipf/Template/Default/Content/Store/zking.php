                    <strong><span style="font-size:19px;font-family: 仿宋_GB2312">保险金额与免赔额（率）</span></strong>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">(</span><span style="font-size:19px;font-family:仿宋_GB2312">一)本保险合同中列明的保险金额应不低于：</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">1</span><span style="font-size:19px;font-family:仿宋_GB2312">、安装工程：保险工程安装完成时的总价值，包括设备费用、原材料费用、安装费、建造费、运输费和保险费、关税、其他税项和费用，以及由工程所有人提供的原材料和设备的费用；</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">2</span><span style="font-size:19px;font-family:仿宋_GB2312">、其他保险项目：由投保人与保险人商定的金额。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">(</span><span style="font-size:19px;font-family:仿宋_GB2312">二)若投保人是以保险工程合同规定的工程概算总造价投保，投保人或被保险人应：</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">1</span><span style="font-size:19px;font-family:仿宋_GB2312">、在本保险项下工程造价中包括的各项费用因涨价或升值原因而超出保险工程造价时，必须尽快以书面通知保险人，保险人据此调整保险金额；</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">2</span><span style="font-size:19px;font-family:仿宋_GB2312">、在保险期间内对相应的工程细节作出精确记录，并允许保险人在合理的时候对该项记录进行查验；</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">3</span><span style="font-size:19px;font-family:仿宋_GB2312">、若保险工程的安装期超过三年，必须从本保险合同生效日起每隔十二个月向保险人申报当时的工程实际投入金额及调整后的工程总造价，保险人将据此调整保险费；</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">4</span><span style="font-size:19px;font-family:仿宋_GB2312">、在本保险合同列明的保险期间届满后三个月内向保险人申报最终的工程总价值，保险人据此以多退少补的方式对预收保险费进行调整。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">免赔额（率）由投保人与保险人在订立保险合同时协商确定，并在保险合同中载明。</span>
                </p>
                <p style="text-align:center;text-indent:40px;line-height:31px">
                    <strong><span style="font-size:19px;font-family: 仿宋_GB2312">赔偿处理</span></strong>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">对保险标的遭受的损失，保险人可选择以支付赔款或以修复、重置受损项目的方式予以赔偿，对保险标的在修复或替换过程中，被保险人进行的任何变更、性能增加或改进所产生的额外费用，保险人不负责赔偿。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">在发生本保险单项下的损失后，保险人按下列方式确定损失金额：</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">（一）可以修复的部分损失：以将保险财产修复至其基本恢复受损前状态的费用考虑本保险合同第四十五条约定的残值处理方式后确定的赔偿金额为准。但若修复费用等于或超过保险财产损失前的价值时，则按下列第（二）款的规定处理；</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">（二）全部损失或推定全损：以保险财产损失前的实际价值考虑本保险合同第四十五条约定的残值处理方式后确定的赔偿金额为准；</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">（三）任何属于成对或成套的设备项目，若发生损失，保险人的赔偿责任不超过该受损项目在所属整对或整套设备项目的保险金额中所占的比例。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">保险标的发生保险责任范围内的损失，保险人按以下方式计算赔偿：</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">（一）保险金额等于或高于应保险金额时，按实际损失计算赔偿，最高不超过应保险金额；</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">（二）保险金额低于应保险金额时，按保险金额与应保险金额的比例乘以实际损失计算赔偿，最高不超过保险金额。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">［标的损失计算按照保险财产损失前的实际价值扣除残值后的金额赔偿；每次事故保险人的赔偿金额为根据第十三条约定计算的金额扣除每次事故免赔额后的金额,或者为根据第十三条约定计算的金额扣除该金额与免赔率乘积后的金额。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">保险标的在连续72小时内遭受暴雨、台风、洪水或其它连续发生的自然灾害所致损失视为一次单独事件，在计算赔偿时视为一次保险事故，并扣减一个相应的免赔额（率）。被保险人可自行决定72小时的起始时间，但若在连续数个72小时时间内发生损失，任何两个或两个以上72小时期限不得重叠。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">若本保险合同所列标的不止一项时，应分项计算赔偿，保险人对每一保险项目的赔偿责任均不得超过本保险合同明细表对应列明的分项保险金额，以及本保险合同特别条款或批单中规定的其他适用的赔偿限额。在任何情况下，保险人在本保险合同下承担的对物质损失的最高赔偿金额不得超过保险合同明细表中列明的总保险金额。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">保险标的的保险金额大于或等于其应保险金额时，被保险人为防止或减少保险标的的损失所支付的必要的、合理的费用，在保险标的损失赔偿金额之外另行计算，最高不超过被施救标的的应保险金额。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">保险标的的保险金额小于其应保险金额时，上述费用按被施救标的的保险金额与其应保险金额的比例在保险标的损失赔偿金额之外另行计算，最高不超过被施救标的的保险金额。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">被施救的财产中，含有本保险合同未承保财产的，按被施救保险标的的应保险金额与全部被施救财产价值的比例分摊施救费用。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">保险标的发生部分损失，保险人履行赔偿义务后，本保险合同的保险金额自损失发生之日起按保险人的赔偿金额相应减少，保险人不退还保险金额减少部分的保险费。如投保人请求恢复至原保险金额，应按原约定的保险费率另行支付恢复部分从投保人请求的恢复日期起至保险期间届满之日止按日比例计算的保险费。&nbsp;&nbsp;&nbsp;&nbsp; </span>
                </p>
                <p style="text-indent:40px;line-height:31px">
                    <strong><span style="font-size:19px;font-family:仿宋_GB2312">&nbsp;</span></strong>
                </p>
                <p style="text-align:center;text-indent:40px;line-height:31px">
                    <strong><span style="font-size:19px;font-family: 仿宋_GB2312">第二部分&nbsp; 第三者责任保险部分</span></strong>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">&nbsp;</span>
                </p>
                <p style="text-align:center;text-indent:40px;line-height:31px">
                    <strong><span style="font-size:19px;font-family: 仿宋_GB2312">保险责任</span></strong>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">在保险期间内，因发生与本保险合同所承保工程直接相关的意外事故引起工地内及邻近区域的第三者人身伤亡、疾病或财产损失,依法应由被保险人承担的经济赔偿责任，保险人按照本保险合同约定负责赔偿。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">本项保险事故发生后，被保险人因保险事故而被提起仲裁或者诉讼的，对应由被保险人支付的仲裁或诉讼费用以及其他必要的、合理的费用（以下简称“法律费用”），经保险人书面同意，保险人按照本保险合同约定也负责赔偿。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">&nbsp;</span>
                </p>
                <p style="text-align:center;text-indent:40px;line-height:31px">
                    <strong><span style="font-size:19px;font-family: 仿宋_GB2312">责任免除</span></strong>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">下列损失、费用，保险人不负责赔偿：</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">（一）本保险合同物质损失项下或本应在该项下予以负责的损失及各种费用； </span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">（二）工程所有人、承包人或其他关系方或其所雇用的在工地现场从事与工程有关工作的职员、工人及上述人员的家庭成员的人身伤亡或疾病；</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">（三）工程所有人、承包人或其他关系方或其所雇用的职员、工人所有的或由上述人员所照管、控制的财产发生的损失；</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">（四）领有公共运输行驶执照的车辆、船舶、航空器造成的事故；</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">（五）被保险人应该承担的合同责任，但无合同存在时仍然应由被保险人承担的法律责任不在此限。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">&nbsp;</span>
                </p>
                <p style="text-align:center;text-indent:40px;line-height:31px">
                    <strong><span style="font-size:19px;font-family: 仿宋_GB2312">责任限额与免赔额（率）</span></strong>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">责任限额包括每次事故责任限额、每人人身伤亡责任限额、累计责任限额，由投保人与保险人协商确定，并在保险合同中载明。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">每次事故免赔额（率）由投保人与保险人在订立保险合同时协商确定，并在保险合同中载明。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">&nbsp;</span>
                </p>
                <p style="text-align:center;text-indent:40px;line-height:31px">
                    <strong><span style="font-size:19px;font-family: 仿宋_GB2312">赔偿处理</span></strong>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">保险人的赔偿以下列方式之一确定的被保险人的赔偿责任为基础：</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">被保险人和向其提出损害赔偿请求的索赔方协商并经保险人确认；</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">仲裁机构裁决；</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">人民法院判决；</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">保险人认可的其他方式。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">在保险期间内发生保险责任范围内的损失，保险人按以下方式计算赔偿：</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">（一）对于每次事故造成的损失，保险人在每次事故责任限额内计算赔偿，其中对每人人身伤亡的赔偿金额不得超过每人人身伤亡责任限额；</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">（二）1、在依据本条第（一）项计算的基础上，保险人在扣除本保险合同载明的每次事故免赔额后进行赔偿，但对于人身伤亡的赔偿不扣除每次事故免赔额；</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">2</span><span style="font-size:19px;font-family:仿宋_GB2312">、在依据本条第（一）项计算的基础上，保险人在扣除按本保险合同载明的每次事故免赔率计算的每次事故免赔额后进行赔偿，但对于人身伤亡的赔偿不扣除每次事故免赔额；</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">（三）保险人对多次事故损失的累计赔偿金额不超过本保险合同列明的累计赔偿限额。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">对每次事故法律费用的赔偿金额，保险人在第二十四条计算的赔偿金额以外按本保险合同的约定另行计算。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">保险人对被保险人给第三者造成的损害，可以依照法律的规定或者合同的约定，直接向该第三者赔偿保险金。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">被保险人给第三者造成损害，被保险人对第三者应负的赔偿责任确定的，根据被保险人的请求，保险人应当直接向该第三者赔偿保险金。被保险人怠于请求的，第三者有权就其应获赔偿部分直接向保险人请求赔偿保险金。被保险人给第三者造成损害，被保险人未向该第三者赔偿的，保险人不得向被保险人赔偿保险金。</span>
                </p>
                <p style="text-align:center;text-indent:40px;line-height:31px">
                    <strong><span style="font-size:19px;font-family:仿宋_GB2312">&nbsp;</span></strong>
                </p>
                <p style="text-align:center;text-indent:40px;line-height:31px">
                    <strong><span style="font-size:19px;font-family: 仿宋_GB2312">第三部分&nbsp; 通用条款</span></strong>
                </p>
                <p style="text-indent:40px;line-height:31px">
                    <strong><span style="font-size:19px;font-family:仿宋_GB2312">&nbsp;</span></strong>
                </p>
                <p style="text-align:center;text-indent:40px;line-height:31px">
                    <strong><span style="font-size:19px;font-family: 仿宋_GB2312">责任免除</span></strong>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">下列原因造成的损失、费用和责任，保险人不负责赔偿：</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">（一）战争、类似战争行为、敌对行为、武装冲突、恐怖活动、谋反、政变；</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">（二）行政行为或司法行为；</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">（三）罢工、暴动、民众骚乱；</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">（四）被保险人及其代表的故意行为或重大过失行为；</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">（五）核裂变、核聚变、核武器、核材料、核辐射、核爆炸、核污染及其他放射性污染；</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">（六）大气污染、土地污染、水污染及其他各种污染。 </span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">下列损失、费用，保险人也不负责赔偿：</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">（一）工程部分停工或全部停工引起的任何损失、费用和责任。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">（二）罚金、延误、丧失合同及其他后果损失；</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">（三）1.本保险合同中载明的免赔额；</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">2.</span><span style="font-size:19px;font-family:仿宋_GB2312">按本保险合同中载明的免赔率计算的免赔额。 </span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">&nbsp;</span>
                </p>
                <p style="text-align:center;text-indent:40px;line-height:31px">
                    <strong><span style="font-size:19px;font-family: 仿宋_GB2312">保险期间</span></strong>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">本保险合同保险期间遵循如下约定：</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">（一）保险人的保险责任自保险工程在工地动工或用于保险工程的材料、设备运抵工地之时起始，至工程所有人对部分或全部工程签发完工验收证书或验收合格，或工程所有人实际占有或使用或接收该部分或全部工程之时终止，以先发生者为准。但在任何情况下，安工期保险责任的起始或终止不得超出本保险合同载明的安工保险期间范围。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">（二）不论有关合同中对试车和考核期如何规定，保险人仅在本保险合同明细表中列明的试车和考核期间内对试车和考核所引发的损失、费用和责任负责赔偿；若保险设备本身是在本次安装前已被使用过的设备或转手设备，则自其试车之时起，保险人对该项设备的保险责任即行终止。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">（三）上述保险期间的展延，投保人须事先获得保险人的书面同意，否则，从本保险合同明细表中列明的安工期保险期间终止日之后发生的任何损失、费用和责任，保险人不负责赔偿。</span>
                </p>
                <p style="text-indent:40px;line-height:31px">
                    <strong><span style="font-size:19px;font-family:仿宋_GB2312">&nbsp;</span></strong>
                </p>
                <p style="text-align:center;text-indent:40px;line-height:31px">
                    <strong><span style="font-size:19px;font-family: 仿宋_GB2312">保险人义务</span></strong>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">订立保险合同时，采用保险人提供的格式条款的，保险人向投保人提供的投保单应当附格式条款，保险人应当向投保人说明保险合同的内容。对保险合同中免除保险人责任的条款，保险人在订立合同时应当在投保单、保险单或者批单上作出足以引起投保人注意的提示，并对该条款的内容以书面或者口头形式向投保人作出明确说明；未作提示或者明确说明的，该条款不产生效力。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">本保险合同成立后，保险人应当及时向投保人签发保险单或批单。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">保险人依据第三十六条所取得的保险合同解除权，自保险人知道有解除事由之日起，超过三十日不行使而消灭。自保险合同成立之日起超过二年的，保险人不得解除合同；发生保险事故的，保险人承担赔偿责任。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">保险人在合同订立时已经知道投保人未如实告知的情况的，保险人不得解除合同；发生保险事故的，保险人应当承担赔偿责任。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">保险人按照第四十二条的约定，认为被保险人提供的有关索赔的证明和资料不完整的，应当及时一次性通知投保人、被保险人补充提供。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">保险人收到被保险人的赔偿保险金的请求后，应当及时作出是否属于保险责任的核定；情形复杂的，应当在三十日内作出核定，但保险合同另有约定的除外。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">保险人应当将核定结果通知被保险人；对属于保险责任的，在与被保险人达成赔偿保险金的协议后十日内，履行赔偿保险金义务。保险合同对赔偿保险金的期限有约定的，保险人应当按照约定履行赔偿保险金的义务。保险人依照前款约定作出核定后，对不属于保险责任的，应当自作出核定之日起三日内向被保险人发出拒绝赔偿保险金通知书，并说明理由。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">保险人自收到赔偿保险金的请求和有关证明、资料之日起六十日内，对其赔偿保险金的数额不能确定的，应当根据已有证明和资料可以确定的数额先予支付；保险人最终确定赔偿的数额后，应当支付相应的差额。</span>
                </p>
                <p style="text-align:center;text-indent:40px;line-height:31px">
                    <strong><span style="font-size:19px;font-family:仿宋_GB2312">&nbsp;</span></strong>
                </p>
                <p style="text-align:center;text-indent:40px;line-height:31px">
                    <strong><span style="font-size:19px;font-family: 仿宋_GB2312">投保人、被保险人义务</span></strong>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">订立保险合同，保险人就保险标的或者被保险人的有关情况提出询问的，投保人应当如实告知。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">投保人故意或者因重大过失未履行前款规定的如实告知义务，足以影响保险人决定是否同意承保或者提高保险费率的，保险人有权解除保险合同。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">投保人故意不履行如实告知义务的，保险人对于合同解除前发生的保险事故，不承担赔偿责任，并不退还保险费。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">投保人因重大过失未履行如实告知义务，对保险事故的发生有严重影响的，保险人对于合同解除前发生的保险事故，不承担赔偿责任，但应当退还保险费。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">投保人应按约定交付保险费。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">约定一次性交付保险费的，投保人在约定交费日后交付保险费的，保险人对交费之前发生的保险事故不承担保险责任。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">约定分期交付保险费的，保险人按照保险事故发生前保险人实际收取保险费总额与投保人应当交付的保险费的比例承担保险责任，投保人应当交付的保险费是指截至保险事故发生时投保人按约定分期应该缴纳的保费总额。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">被保险人应当遵守国家有关消防、安全、生产操作等方面的相关法律、法规及规定，谨慎选用施工人员，遵守一切与施工有关的法规、技术规程和安全操作规程，维护保险标的的安全。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">保险人及其代表有权在适当的时候对保险标的的风险情况进行现场查验。被保险人应提供一切便利及保险人要求的用以评估有关风险的详情和资料，但上述查验并不构成保险人对被保险人的任何承诺。保险人向投保人、被保险人提出消除不安全因素和隐患的书面建议，投保人、被保险人应该认真付诸实施。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">投保人、被保险人未按照约定履行其对保险标的的安全应尽责任的，保险人有权要求增加保险费或者解除合同。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">保险标的转让的，被保险人或者受让人应当及时通知保险人。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">因保险标的转让导致危险程度显著增加的，保险人自收到前款规定的通知之日起三十日内，可以按照合同约定增加保险费或者解除合同。保险人解除合同的，应当将已收取的保险费，按照合同约定扣除自保险责任开始之日起至合同解除之日止应收的部分后，退还投保人。&nbsp;&nbsp; 被保险人、受让人未履行本条规定的通知义务的，因转让导致保险标的危险程度显著增加而发生的保险事故，保险人不承担赔偿责任。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">在保险期间内，被保险人在工程设计、施工方式、工艺、技术手段等方面发生改变致使保险工程风险程度显著增加或其他足以影响保险人决定是否继续承保或是否增加保险费的保险合同重要事项变更，被保险人应及时书面通知保险人，保险人有权要求增加保险费或者解除合同。保险人解除合同的，应当将已收取的保险费，按照合同约定扣除自保险责任开始之日起至合同解除之日止应收的部分后，退还投保人。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">被保险人未履行通知义务，因上述保险合同重要事项变更而导致保险事故发生的，保险人不承担赔偿责任。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">投保人、被保险人知道保险事故发生后，应当立即通知保险人，被保险人应该：</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">（一）尽力采取必要、合理的措施，防止或减少损失，否则，对因此扩大的损失，保险人不承担赔偿责任；</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">（二）立即通知保险人，并书面说明事故发生的原因、经过和损失情况；故意或者因重大过失未及时通知，致使保险事故的性质、原因、损失程度等难以确定的，保险人对无法确定的部分，不承担赔偿责任，但保险人通过其他途径已经及时知道或者应当及时知道保险事故发生的除外；</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">（三）保护事故现场，允许并且协助保险人进行事故调查；对于拒绝或者妨碍保险人进行事故调查导致无法认定事故原因或核实损失情况的，保险人对无法核实的部分不承担赔偿责任；</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">（四）在保险财产遭受盗窃或恶意破坏时，立即向公安部门报案；</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">（五）在预知可能引起第三者责任险项下的诉讼时，立即以书面形式通知保险人，并在接到法院传票或其他法律文件后，立即将其送交保险人。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">被保险人向保险人请求赔偿时，应向保险人提交保险单、索赔申请、财产损失清单、有关部门的损失证明以及其他投保人、被保险人所能提供的与确认保险事故的性质、原因、损失程度等有关的证明和资料。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">投保人、被保险人未履行前款约定的索赔材料提供义务，导致保险人无法核实损失情况的，保险人对无法核实的部分不承担赔偿责任。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">若在某一保险财产中发现的缺陷表明或预示类似缺陷亦存在于其他保险财产中时，被保险人应立即自付费用进行调查并纠正该缺陷。否则，由该缺陷或类似缺陷造成的损失保险人不承担赔偿责任。</span>
                </p>
                <p style="text-align:center;text-indent:40px;line-height:31px">
                    <strong><span style="font-size:19px;font-family:仿宋_GB2312">&nbsp;</span></strong>
                </p>
                <p style="text-align:center;text-indent:40px;line-height:31px">
                    <strong><span style="font-size:19px;font-family: 仿宋_GB2312">赔偿处理</span></strong>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">保险事故发生时，被保险人对保险标的不具有保险利益的，不得向保险人请求赔偿保险金。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">保险标的遭受损失后，如果有残余价值，应由双方协商处理。若协商残值归被保险人所有，应在赔偿金额中扣减残值。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">保险事故发生时，如果存在重复保险，保险人按照本保险合同的相应保险金额与其他保险合同及本保险合同相应保险金额总和的比例承担赔偿责任。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">其他保险人应承担的赔偿金额，本保险人不负责垫付。若被保险人未如实告知导致保险人多支付赔偿金的，保险人有权向被保险人追回多支付的部分。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">发生保险责任范围内的损失，应由有关责任方负责赔偿的，保险人自向被保险人赔偿保险金之日起，在赔偿金额范围内代位行使被保险人对有关责任方请求赔偿的权利，被保险人应当向保险人提供必要的文件和所知道的有关情况。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">被保险人已经从有关责任方取得赔偿的，保险人赔偿保险金时，可以相应扣减被保险人已从有关责任方取得的赔偿金额。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">保险事故发生后，在保险人未赔偿保险金之前，被保险人放弃对有关责任方请求赔偿权利的，保险人不承担赔偿责任；保险人向被保险人赔偿保险金后，被保险人未经保险人同意放弃对有关责任方请求赔偿权利的，该行为无效；由于被保险人故意或者因重大过失致使保险人不能行使代位请求赔偿的权利的，保险人可以扣减或者要求返还相应的保险金。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">被保险人向保险人请求赔偿的诉讼时效期间为二年，自其知道或者应当知道保险事故发生之日起计算。</span><span style="font-size:19px;font-family:宋体">&nbsp;</span>
                </p>
                <p style="text-align:center;text-indent:40px;line-height:31px">
                    <strong><span style="font-size:19px;font-family:仿宋_GB2312">&nbsp;</span></strong>
                </p>
                <p style="text-align:center;text-indent:40px;line-height:31px">
                    <strong><span style="font-size:19px;font-family: 仿宋_GB2312">争议处理</span></strong>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">因履行本保险合同发生的争议，由当事人协商解决。协商不成的，提交保险单载明的仲裁机构仲裁；保险单未载明仲裁机构且争议发生后未达成仲裁协议的，依法向人民法院起诉。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">与本保险合同有关的以及履行本保险合同产生的一切争议，适用中华人民共和国法律（不包括港澳台地区法律）。</span>
                </p>
                <p style="text-align:center;text-indent:40px;line-height:31px">
                    <strong><span style="font-size:19px;font-family:仿宋_GB2312">&nbsp;</span></strong>
                </p>
                <p style="text-align:center;text-indent:40px;line-height:31px">
                    <strong><span style="font-size:19px;font-family: 仿宋_GB2312">其他事项</span></strong>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">保险标的发生部分损失的，自保险人赔偿之日起三十日内，投保人可以解除合同；除合同另有约定外，保险人也可以解除合同，但应当提前十五日通知投保人。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">保险合同依据前款规定解除的，保险人应当将保险标的未受损失部分的保险费，按照合同约定扣除自保险责任开始之日起至合同解除之日止应收的部分后，退还投保人。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">保险责任开始前，投保人要求解除保险合同的，应当按本保险合同的约定向保险人支付手续费，保险人应当退还保险费。保险人要求解除保险合同的，不得向投保人收取手续费并应退还已收取的保险费。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">保险责任开始后，投保人要求解除保险合同的，自通知保险人之日起，保险合同解除，保险人按照保险责任开始之日起至合同解除之日止期间与保险期间的日比例计收保险费，并退还剩余部分保险费；保险人要求解除保险合同的，应提前十五日向投保人发出解约通知书，保险人按照保险责任开始之日起至合同解除之日止期间与保险期间的日比例计收保险费，并退还剩余部分保险费。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">保险标的发生全部损失，属于保险责任的，保险人在履行赔偿义务后，本保险合同终止；不属于保险责任的，本保险合同终止，保险人按照保险责任开始之日起至合同解除之日止期间与保险期间的日比例计收保险费，并退还剩余部分保险费。</span>
                </p>
                <p style="text-align:center;text-indent:40px;line-height:31px">
                    <strong><span style="font-size:19px;font-family:仿宋_GB2312">&nbsp;</span></strong>
                </p>
                <p style="text-align:center;text-indent:40px;line-height:31px">
                    <strong><span style="font-size:19px;font-family: 仿宋_GB2312">释义</span></strong>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">本保险合同涉及下列术语时，适用下列释义： </span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">（一）自然灾害：指地震、海啸、雷击、暴雨、洪水、暴风、龙卷风、冰雹、台风、飓风、沙尘暴、暴雪、冰凌、突发性滑坡、崩塌、泥石流、地面突然下陷下沉及其他人力不可抗拒的破坏力强大的自然现象。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">1</span><span style="font-size:19px;font-family:仿宋_GB2312">、地震：指地下岩石的构造活动或火山爆发产生的地面震动。由于地震的强度不同，其破坏力也存在很大的区别，一般保险针对的是破坏性地震，根据国家地震局的有关规定，震级在4.75级以上且烈度在6级以上的地震为破坏性地震。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">2</span><span style="font-size:19px;font-family:仿宋_GB2312">、海啸：指由于地震或风暴而造成的海面巨大涨落现象，按成因分为地震海啸和风暴海啸两种。地震海啸是伴随地震而形成的，即海底地壳发生断裂，引起剧烈的震动，产生巨大的波浪。风暴海啸是强大低气压在通过时，海面异常升起的现象。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">3</span><span style="font-size:19px;font-family:仿宋_GB2312">、雷击指由雷电造成的灾害。雷电为积雨云中、云间或云地之间产生的放电现象。雷击的破坏形式分直接雷击与感应雷击两种。</span>
                </p>
                <p style="text-align:left;text-indent:19px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">（1）直接雷击：由于雷电直接击中保险标的造成损失，属直接雷击责任。</span>
                </p>
                <p style="text-align:left;text-indent:19px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">（2）感应雷击：由于雷击产生的静电感应或电磁感应使屋内对地绝缘金属物体产生高电位放出火花引起的火灾，导致电器本身的损毁，或因雷电的高电压感应，致使电器部件的损毁，属感应雷击责任。&nbsp;&nbsp;&nbsp; </span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">4</span><span style="font-size:19px;font-family:仿宋_GB2312">、暴雨：指每小时降雨量达16毫米以上，或连续12小时降雨量达30毫米以上，或连续24小时降雨量达50毫米以上的降雨。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">5</span><span style="font-size:19px;font-family:仿宋_GB2312">、洪水：指山洪暴发、江河泛滥、潮水上岸及倒灌。但规律性的涨潮、自动灭火设施漏水以及在常年水位以下或地下渗水、水管暴裂不属于洪水责任。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">6</span><span style="font-size:19px;font-family:仿宋_GB2312">、暴风：指风力达8级、风速在17.2米/秒以上的自然风。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">7</span><span style="font-size:19px;font-family:仿宋_GB2312">、龙卷风：指一种范围小而时间短的猛烈旋风，陆地上平均最大风速在79米/秒-103米/秒，极端最大风速在100米/秒以上。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">8</span><span style="font-size:19px;font-family:仿宋_GB2312">、冰雹：指从强烈对流的积雨云中降落到地面的冰块或冰球，直径大于5毫米，核心坚硬的固体降水。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">9</span><span style="font-size:19px;font-family:仿宋_GB2312">、台风、飓风：台风指中心附近最大平均风力12级或以上，即风速在32.6米/秒以上的热带气旋；飓风是一种与台风性质相同、但出现的位置区域不同的热带气旋，台风出现在西北太平洋海域，而飓风出现在印度洋、大西洋海域。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">10</span><span style="font-size:19px;font-family:仿宋_GB2312">、沙尘暴：指强风将地面大量尘沙吹起，使空气很混浊，水平能见度小于1公里的天气现象。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">11</span><span style="font-size:19px;font-family:仿宋_GB2312">、暴雪：指连续12小时的降雪量大于或等于10毫米的降雪现象。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">12</span><span style="font-size:19px;font-family:仿宋_GB2312">、冰凌：指春季江河解冻期时冰块飘浮遇阻，堆积成坝，堵塞江道，造成水位急剧上升，以致江水溢出江道，漫延成灾。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">陆上有些地区，如山谷风口或酷寒致使雨雪在物体上结成冰块，成下垂形状，越结越厚，重量增加，由于下垂的拉力致使物体毁坏，也属冰凌责任。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">13</span><span style="font-size:19px;font-family:仿宋_GB2312">、突发性滑坡：斜坡上不稳的岩土体或人为堆积物在重力作用下突然整体向下滑动的现象。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">14</span><span style="font-size:19px;font-family:仿宋_GB2312">、崩塌：石崖、土崖、岩石受自然风化、雨蚀造成崩溃下塌，以及大量积雪在重力作用下从高处突然崩塌滚落。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">15</span><span style="font-size:19px;font-family:仿宋_GB2312">、泥石流：由于雨水、冰雪融化等水源激发的、含有大量泥沙石块的特殊洪流。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">16</span><span style="font-size:19px;font-family:仿宋_GB2312">、地面突然下陷下沉：地壳因为自然变异，地层收缩而发生突然塌陷。对于因海潮、河流、大雨侵蚀或在建筑房屋前没有掌握地层情况，地下有孔穴、矿穴，以致地面突然塌陷，也属地面突然下陷下沉。但未按建筑施工要求导致建筑地基下沉、裂缝、倒塌等，不在此列。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">（二）意外事故：指不可预料的以及被保险人无法控制并造成物质损失或人身伤亡的突发性事件，包括火灾和爆炸。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">1</span><span style="font-size:19px;font-family:仿宋_GB2312">、火灾</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">在时间或空间上失去控制的燃烧所造成的灾害。构成本保险的火灾责任必须同时具备以下三个条件：</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">（1）有燃烧现象，即有热有光有火焰；</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">（2）偶然、意外发生的燃烧；</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">（3）燃烧失去控制并有蔓延扩大的趋势。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">因此，仅有燃烧现象并不等于构成本保险中的火灾责任。在生产、生活中有目的用火，如为了防疫而焚毁玷污的衣物，点火烧荒等属正常燃烧，不同于火灾责任。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">因烘、烤、烫、烙造成焦糊变质等损失，既无燃烧现象，又无蔓延扩大趋势，也不属于火灾责任。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">电机、电器、电气设备因使用过度、超电压、碰线、孤花、漏电、自身发热所造成的本身损毁，不属于火灾责任。但如果发生了燃烧并失去控制蔓延扩大，才构成火灾责任，并对电机、电器、电气设备本身的损失负责赔偿。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">2</span><span style="font-size:19px;font-family:仿宋_GB2312">、爆炸</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">爆炸分物理性爆炸和化学性爆炸。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">（1）物理性爆炸：由于液体变为蒸汽或气体膨胀，压力急剧增加并大大超过容器所能承受的极限压力，因而发生爆炸。如锅炉、空气压缩机、压缩气体钢瓶、液化气罐爆炸等。关于锅炉、压力容器爆炸的定义是：锅炉或压力容器在使用中或试压时发生破裂，使压力瞬时降到等于外界大气压力的事故，称为“爆炸事故”。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">（2）化学性爆炸：物体在瞬息分解或燃烧时放出大量的热和气体，并以很大的压力向四周扩散的现象。如火药爆炸、可燃性粉尘纤维爆炸、可燃气体爆炸及各种化学物品的爆炸等。</span>
                </p>
                <p style="text-align:left;text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">因物体本身的瑕疵，使用损耗或产品质量低劣以及由于容器内部承受“负压”（内压比外压小）造成的损失，不属于爆炸责任。</span>
