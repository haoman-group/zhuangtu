                    <span style="font-size:19px;font-family: 仿宋_GB2312">（二）因保险事故引起的任何精神损失或间接损失。</span>
                </p>
                <p style="text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family: 仿宋_GB2312">第十条&nbsp; 其他不属于本合同保险责任范围内的损失和费用，保险人不负责赔偿。</span>
                </p>
                <p style="text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">&nbsp;</span>
                </p>
                <p style="text-align:center;text-indent:40px;line-height:31px">
                    <strong><span style="font-size:19px;font-family: 仿宋_GB2312">保险金额及免赔额</span></strong>
                </p>
                <p style="text-indent:37px;line-height:31px;text-autospace:none">
                    <span style="font-size:19px;font-family:仿宋_GB2312">第十一条&nbsp; 各项保险财产的保险金额由投保人与保险人协商确定，并在保险单上分项载明。</span>
                </p>
                <p style="text-indent:37px;line-height:31px;text-autospace:none">
                    <span style="font-size:19px;font-family:仿宋_GB2312">保险金额不得超过保险价值；超过保险价值的，超过部分无效。</span>
                </p>
                <p style="text-indent:37px;line-height:31px;text-autospace:none">
                    <span style="font-size:19px;font-family:仿宋_GB2312">投保人只有在投保本条款第二条第（一）、（二）、（三）款财产之后方可投保便携式家用电器和现金首饰类财产，便携式家用电器和现金首饰类财产保险金额之和最高不超过本条款第二条第（一）、（二）、（三）款财产保险金额总和的10%。</span>
                </p>
                <p style="text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family: 仿宋_GB2312">第十二条&nbsp; 投保人与保险人在订立本合同时可约定一定的免赔额，并在保险单上列明。</span>
                </p>
                <p style="text-indent:37px;line-height:31px;text-autospace:none">
                    <span style="font-size:19px;font-family:仿宋_GB2312">第十三条&nbsp; 在保险期间内，在中华人民共和国法定节假日期间，根据投保人和保险人的约定，本合同和各附加险合同的各项保险责任所对应的保险金额、赔偿限额自动提升10%，但相应的免赔额不变。</span>
                </p>
                <p style="text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">&nbsp;</span>
                </p>
                <p style="text-align:center;text-indent:40px;line-height:31px">
                    <strong><span style="font-size:19px;font-family: 仿宋_GB2312">保险期间</span></strong>
                </p>
                <p style="text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family: 仿宋_GB2312">第十四条&nbsp; 本合同的保险期间为一年，自保险单载明的保险责任起始日零时起至约定的保险责任终止日二十四时止。</span>
                </p>
                <p style="text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family: 仿宋_GB2312">第十五条&nbsp; 投保人在投保时可以选择自动续保。投保人选择自动续保且根据本合同约定在保险期间届满前足额交付了下一年度保险费或下一年度第一期保险费的，本合同到期后保险期间将自动延续一年。</span>
                </p>
                <p style="text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">&nbsp;</span>
                </p>
                <p style="text-align:center;text-indent:40px;line-height:31px">
                    <strong><span style="font-size:19px;font-family: 仿宋_GB2312">保险人义务</span></strong>
                </p>
                <p style="text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family: 仿宋_GB2312">第十六条&nbsp; </span><span style="font-size:19px;font-family:仿宋_GB2312">本保险合同成立后，保险人应当及时向投保人签发保险单或其他保险凭证。</span>
                </p>
                <p style="text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">第十七条&nbsp; 保险人按照本保险合同的约定，认为被保险人提供的有关索赔的证明和资料不完整的，应当及时一次性通知投保人、被保险人补充提供。</span>
                </p>
                <p style="text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family: 仿宋_GB2312">第十八条&nbsp; </span><span style="font-size:19px;font-family:仿宋_GB2312">保险人收到被保险人的赔偿保险金的请求后，应当及时作出核定；情形复杂的，保险人应当在三十日内作出核定；保险人未能在三十日内作出核定的，应与被保险人商定合理期间，并在商定期间内作出核定，同时将核定结果及时通知被保险人。</span>
                </p>
                <p style="text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">对属于保险责任的，在与被保险人达成赔偿保险金的协议后十日内，履行赔偿保险金义务。保险合同对赔偿保险金的期限有约定的，保险人应当按照约定履行赔偿保险金的义务。保险人依照前款约定作出核定后，对不属于保险责任的，应当自作出核定之日起三日内向被保险人发出拒绝赔偿保险金通知书，并说明理由。</span>
                </p>
                <p style="text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family: 仿宋_GB2312">第十九条&nbsp; 在本合同有效期内，除</span><span style="font-size:19px;font-family:仿宋_GB2312">法律规定或合同约定投保人不得解除本合同的情况外，</span><span style="font-size:19px;font-family:仿宋_GB2312">投保人要求解除本合同时，保险人应当及时办理退保手续。</span>
                </p>
                <p style="text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family: 仿宋_GB2312">第二十条&nbsp; </span><span style="font-size:19px;font-family:仿宋_GB2312">在本合同有效期内，投保人与保险人经协商同意，可以变更本合同的有关内容。保险人应当</span><span style="font-size:19px;font-family:仿宋_GB2312">在原保险单或者其他保险凭证上批注或者附贴批单，或者由投保人和保险人订立变更的书面协议</span><span style="font-size:19px;font-family:仿宋_GB2312">。</span>
                </p>
                <p style="text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family: 仿宋_GB2312">第二十一条&nbsp; </span><span style="font-size:19px;font-family:仿宋_GB2312">保险人自收到赔偿保险金的请求和有关证明、资料之日起六十日内，对其赔偿保险金的数额不能确定的，应当根据已有证明和资料可以确定的最低数额先予支付；保险人最终确定给付保险金的数额后，应当支付相应的差额。</span>
                </p>
                <p style="text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">&nbsp;</span>
                </p>
                <p style="text-align:center;text-indent:40px;line-height:31px">
                    <strong><span style="font-size:19px;font-family: 仿宋_GB2312">投保人与被保险人义务</span></strong>
                </p>
                <p style="text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family: 仿宋_GB2312">第二十二条&nbsp; </span><span style="font-size:19px;font-family:仿宋_GB2312;color:black">订立保险合同，保险人就保险标的或者被保险人的有关情况提出询问的，投保人应当如实告知。</span>
                </p>
                <p style="text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family: 仿宋_GB2312;color:black">投保人故意或者因重大过失未履行前款规定的如实告知义务，足以影响保险人决定是否同意承保或者提高保险费率的，保险人有权解除保险合同。</span>
                </p>
                <p style="text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family: 仿宋_GB2312;color:black">前款规定的合同解除权，自保险人知道有解除事由之日起，超过三十日不行使而消灭。自合同成立之日起超过二年的，保险人不得解除合同；发生保险事故的，保险人应当承担赔偿或者给付保险金的责任。<br/> &nbsp;&nbsp;&nbsp; 投保人故意不履行如实告知义务的，保险人对于合同解除前发生的保险事故，不承担赔偿或者给付保险金的责任，并不退还保险费。</span>
                </p>
                <p style="text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family: 仿宋_GB2312;color:black">投保人因重大过失未履行如实告知义务，对保险事故的发生有严重影响的，保险人对于合同解除前发生的保险事故，不承担赔偿或者给付保险金的责任，但应当退还保险费。</span>
                </p>
                <p style="text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family: 仿宋_GB2312;color:black">保险人在合同订立时已经知道投保人未如实告知的情况的，保险人不得解除合同；发生保险事故的，保险人应当承担赔偿或者给付保险金的责任。</span>
                </p>
                <p style="text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family: 仿宋_GB2312">第二十三条&nbsp; 投保人应按照本合同的约定交付保险费。本合同约定一次性交付保险费或对保险费交付方式、交付时间没有约定的，投保人应在保险责任起始日前一次性交付保险费；约定以分期付款方式交付保险费的，投保人应按期交付第一期保险费。投保人未按本款约定交付保险费的，本合同不生效，保险人不承担保险责任。</span>
                </p>
                <p style="text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family: 仿宋_GB2312">如果发生投保人未按期足额交付保险费或不按约定日期交付第二期或以后任何一期保险费的情形, 从违约之日起，保险人有权解除本合同并追收已经承担保险责任期间的保险费和利息，本合同自解除通知送达投保人时解除；在本合同解除前发生保险事故的，保险人按投保人已付保险费占保险单中载明的总保险费的比例承担保险责任。</span>
                </p>
                <p style="text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family: 仿宋_GB2312">第二十四条&nbsp; 发生保险事故时，被保险人应当积极抢救，尽可能使损失减少至最低限度，同时及时向公安、消防等有关部门报案并立即通知保险人。<span style="color:black">故意或者因重大过失未及时通知，致使保险事故的性质、原因、损失程度等难以确定的，保险人对无法确定的部分，不承担赔偿或者给付保险金的责任，但保险人通过其他途径已经及时知道或者应当及时知道保险事故发生的除外。</span></span>
                </p>
                <p style="text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family: 仿宋_GB2312">第二十五条&nbsp; </span><span style="font-size:19px;font-family:仿宋_GB2312">在本合同有效期内，保险地址或被保险人发生变更时，被保险人应当以书面形式及时通知保险人，保险人有权要求增加保险费或者解除合同。</span>
                </p>
                <p style="text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family: 仿宋_GB2312">被保险人未履行上述通知义务的，保险人对前款事项变更后发生的保险事故不再承担赔偿责任。</span>
                </p>
                <p style="text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family: 仿宋_GB2312">第二十六条&nbsp; </span><span style="font-size:19px;font-family:仿宋_GB2312">在保险期间内，本合同载明的重要事项发生变更或保险标的危险程度显著增加的，投保人、被保险人应及时书面通知保险人</span><span style="font-size:19px;font-family:仿宋_GB2312">。</span>
                </p>
                <p style="text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">被保险人未履行前款约定的义务，并且变更事项导致保险事故或与保险事故相关时，保险人有权拒绝赔偿；如果变更事项增加了保险人所承担的风险，保险人有权要求增加保险费或者解除本合同。</span><span style="font-size:19px;font-family:仿宋_GB2312;color:black">保险人解除合同的，应当将已收取的保险费，按照合同约定扣除自保险责任开始之日起至合同解除之日止应收的部分后，退还投保人。</span>
                </p>
                <p style="text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">第二十七条&nbsp; 保险标的转让的，被保险人或者受让人应当及时通知保险人。</span>
                </p>
                <p style="text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">因保险标的转让导致危险程度显著增加的，保险人自收到前款规定的通知之日起三十日内，可以按照合同约定增加保险费或者解除合同。</span>
                </p>
                <p style="text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">被保险人、受让人未履行本条规定的通知义务的，因转让导致保险标的危险程度显著增加而发生的保险事故，保险人不承担赔偿保险金的责任。</span>
                </p>
                <p style="text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family: 仿宋_GB2312">第二十八条&nbsp; 被保险人应及时向保险人提供与索赔相关的各种真实的证明、票据、文件和资料。</span>
                </p>
                <p style="text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family: 仿宋_GB2312">因被保险人未履行</span><span style="font-size:19px;font-family:仿宋_GB2312">前款约定的义务</span><span style="font-size:19px;font-family:仿宋_GB2312">，导致部分或全部保险责任无法确定，保险人对无法确定的部分不承担赔偿责任。</span>
                </p>
                <p style="text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">&nbsp;</span>
                </p>
                <p style="text-align:center;text-indent:40px;line-height:31px">
                    <strong><span style="font-size:19px;font-family: 仿宋_GB2312">赔偿处理</span></strong>
                </p>
                <p style="text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family: 仿宋_GB2312">第二十九条&nbsp; 保险事故发生时，被保险人对保险标的不具有保险利益的，不得向保险人请求赔偿保险金。</span>
                </p>
                <p style="text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">第三十条&nbsp; 发生保险责任范围内的损失时，被保险人应当尽可能修复。修复前，被保险人须会同保险人检修，确定修理项目、方式或费用。否则，保险人有权重新核定或拒绝赔偿。</span>
                </p>
                <p style="text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family: 仿宋_GB2312">第三十一条&nbsp; 保险事故发生后，保险人按照下列方式计算赔偿：</span>
                </p>
                <p style="text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family: 仿宋_GB2312">（一）本合同第二条列明的各项保险财产的赔偿计算：</span>
                </p>
                <p style="text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family: 仿宋_GB2312">在各项保险财产分项保险金额内，按实际损失赔偿。</span>
                </p>
                <p style="text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family: 仿宋_GB2312">（二）保险人对施救费用的赔偿在保险财产损失以外另行计算，但最高不超过获救保险财产的分项保险金额。</span>
                </p>
                <p style="text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family: 仿宋_GB2312">如果获救财产中含有不属于本合同保险财产的，并且无法区分施救费用是因抢救何种财产而产生的，保险人将按照获救保险财产实际价值占全部获救财产实际价值的比例赔偿施救费用。</span>
                </p>
                <p style="text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">第三十二条&nbsp; 如存在重复保险的情况，保险人将按照本合同保险金额占全部相关保险合同保险金额总和的比例承担赔偿责任。</span>
                </p>
                <p style="text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">应由其他保险人承担的赔偿金额，保险人不负责垫付。</span>
                </p>
                <p style="text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family:仿宋_GB2312">第三十三条&nbsp; 属于本合同项下的赔偿责任涉及其他责任方时，保险人自向被保险人赔偿保险金之日起，在赔偿金额范围内代位行使被保险人对其他责任方请求赔偿的权利。被保险人已经从其他责任方取得赔偿的，保险人赔偿保险金时，可以相应扣减被保险人已从其他责任方取得的赔偿金额。</span>
                </p>
                <p style="text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family: 仿宋_GB2312">保险事故发生后，在保险人未赔偿保险金之前，被保险人放弃对其他责任方请求赔偿权利的，保险人不承担赔偿责任；保险人向被保险人赔偿保险金后，被保险人未经保险人同意放弃对其他责任方请求赔偿权利的，该行为无效；由于被保险人的过错致使保险人不能行使代位请求赔偿权的，保险人可以相应扣减保险赔偿金。</span>
                </p>
                <p style="text-indent:37px;line-height:31px">
                    <span style="font-size:19px;font-family: 仿宋_GB2312">在保险人向其他责任方行使代位请求赔偿权时，被保险人应向保险人告知其知晓的有关情况并根据保险人的要求提供必要的证明和资料，积极协助保险人追偿。</span>
                </p>
                <p style="text-indent:37px;line-height:31px">
